
public class Equipement  {
	protected int force;
	protected int adresse;
	protected String nom;
	protected int Resistance;
	
public Equipement() {
	
}
	
public Equipement (String nom) {
	this.nom = nom;
}

public int getForce() {
	return this.force;
}

public int getadresse() {
	return this.adresse;
}

public int getResistance() {
	return this.Resistance;
}


}
